let x = 10;

console.log("Initial value:", x);     //10

x += 5;   // x = x + 5
console.log("After += :", x);         //15

x -= 3;   // x = x - 3
console.log("After -= :", x);         //12

x *= 2;   // x = x * 2
console.log("After *= :", x);         //24

x /= 4;   // x = x / 4
console.log("After /= :", x);         //6

x %= 3;   // x = x % 3
console.log("After %= :", x);         //0
